#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*res;
	char	cc;
	int		i;

	res = NULL;
	cc = (char) c;
	i = 0;
	while (s[i])
	{
		if (s[i] == cc)
			res = (char *) &s[i];
		i++;
	}
	if (s[i] == cc)
		res = (char *) &s[i];
	return (res);
}
